/** Busca la opción del Anexo C que coincide con un set de 8 letras de grupo (los que producen los 3ros qualifying). */
export function lookupAnnexC(qualifyingGroups: Set<string>): AnnexCOption | null {
  if (qualifyingGroups.size !== 8) return null;
  for (const opt of ANNEX_C) {
    const setT = new Set(opt.thirds);
    if (setT.size !== 8) continue;
    let match = true;
    for (const g of qualifyingGroups) {
      if (!setT.has(g)) { match = false; break; }
    }
    if (match) return opt;
  }
  return null;
}